def caser(direction,text,shift):
    enc_text = ""
    for i in range(len(text)):
        if text[i] == ' ':
            enc_text += text[i]
        else:
            index = alphabet.index(text[i])
            if direction=='encode':
                index += shift
            elif direction=='decode':
                index -= shift
            index = index % len(alphabet)
            enc_text += str(alphabet[index])
    if direction == 'encode':
        print(f"The encoded text is {enc_text}")
    elif direction == 'decode':
        print(f"The decoded text is {enc_text}")


